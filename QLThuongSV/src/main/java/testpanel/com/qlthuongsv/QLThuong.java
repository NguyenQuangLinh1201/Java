    private void fillTable(){
        tableModel.setRowCount(0);
        for (Student student : listStudent) {
            tableModel.addRow(new String[]{student.getStudentID(),student.getName(),""+student.getMark(),student.getMajor(),student.getCapacity(),student.getGioiTinh()});
        }
        tableModel.fireTableDataChanged();
    }

