    private void btlAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlAddActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtMSV.getText().equals(""))
            sb.append("Ma SV khong duoc de trong\n");
        if(txtName.getText().equals(""))
            sb.append("Ho ten sinh vien khong dc de trong");
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }    
        Student st1 = new Student();
        st1.setStudentID(txtMSV.getText());
        st1.setName(txtName.getText());
        st1.setMajor(CBNganh.getSelectedItem().toString());
        st1.setMark(Float.parseFloat(txtDiem.getText()));
        
        listStudent.add(st1);
        fillTable();
    }//GEN-LAST:event_btlAddActionPerformed

    private void btlXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtMSV.getText().equals(""))
            sb.append("Ma SV khong duoc de trong\n");
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }    
        int choice = JOptionPane.showConfirmDialog(this, "Ban co muon xoa thong tin sinh vien nay khong","Hoi",JOptionPane.YES_NO_OPTION);
        if(choice == JOptionPane.NO_OPTION){
            return;
        }
        for (Student student : listStudent) {
            if(student.getStudentID().equals(txtMSV.getText())){
                listStudent.remove(student);
                break;
            }
        }
        fillTable();
        btlResetActionPerformed(evt);
    }//GEN-LAST:event_btlXoaActionPerformed

    private void btlUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlUpdateActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtMSV.getText().equals(""))
            sb.append("Ma SV khong duoc de trong\n");
        if(txtName.getText().equals(""))
            sb.append("Ho ten sinh vien khong dc de trong");
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }    
        int choice = JOptionPane.showConfirmDialog(this, "Ban co muon sua thong tin sinh vien nay khong","Hoi",JOptionPane.YES_NO_OPTION);
        if(choice == JOptionPane.NO_OPTION){
            return;
        }
        for (Student student : listStudent) {
            if(student.getStudentID().equals(txtMSV.getText())){
                student.setName(txtName.getText());
                student.setMajor(CBNganh.getSelectedItem().toString());
                student.setMark(Float.parseFloat(txtDiem.getText()));
                student.setMajor((String) CBNganh.getSelectedItem());
                break;
            }
        }
        fillTable();
        btlResetActionPerformed(evt);
    }//GEN-LAST:event_btlUpdateActionPerformed

